import androidx.navigation.NavHostController
import androidx.navigation.compose.*
import com.example.myapplication.ui.theme.MyApplicationTheme

class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        setContent {
            MyApplicationTheme {
                AppNavigation()
            }
        }
    }
}

@Composable
fun AppNavigation() {
    val navController = rememberNavController()
    NavHost(navController = navController, startDestination = "splash") {
        composable("splash") { SplashScreen(navController) }
        composable("components") { ComponentListScreen(navController) }
        composable("textDetail") { TextDetailScreen(navController) }
        composable("imageDetail") { ImageDetail(navController) }
        composable("layoutScreen") { LayoutScreen(navController) }
        composable("TextFieldScreen") { TextFieldScreen(navController) }

    }
}
